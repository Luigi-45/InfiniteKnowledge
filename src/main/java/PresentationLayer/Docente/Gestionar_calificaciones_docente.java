    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try{
            if(this.jtxtEstudiante.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Error, debe seleccionar un estudiante");
            }
            else if(this.jtxtSalonClases.getText().length()!=2){
                JOptionPane.showMessageDialog(null, "Error, el sal贸n de clases ingresado no posee la cantidad de caracteres adecuada (2)");
            }
            else{
                RegistroCalificacionesBO registroCalificacionesBO = new RegistroCalificacionesBO();
                DocenteBO docenteBO = new DocenteBO();
                CursoBO cursoBO = new CursoBO();
                
                RegistroCalificaciones registroCalificaciones = new RegistroCalificaciones(String.valueOf(docenteBO.buscarPorDNI(this.getDni()).getIdMiembro()), 
                String.valueOf(this.estudiante.getIdMiembro()), String.valueOf(cursoBO.buscarPorNombreExacto(String.valueOf(this.listaCursos.getSelectedItem())).getCursoId()), 
                this.jtxtSalonClases.getText(),(String) this.jcoBimestre.getSelectedItem());
                
                registroCalificaciones.setFechaEmision(new Date());
                
                registroCalificacionesBO.insertar(registroCalificaciones);
                
                JOptionPane.showMessageDialog(null, "El registro de calificaciones de ha ingresado correctamente");
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

