    private void jtxtNombreCompletoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtNombreCompletoKeyTyped
        if(evt.getKeyChar()== KeyEvent.VK_ENTER){
            try{
                vaciarTabla();

                EstudianteBO estudianteBO = new EstudianteBO();
                ArrayList<Estudiante> estudiantes = estudianteBO.buscar_por_nombre_completo(this.jtxtNombreCompleto.getText());

                for(Estudiante i:estudiantes){
                    String[] datos = {i.getDni(), i.getNombreCompleto(), i.getGenero()};
                    this.modelo.addRow(datos);
                }

            }
            catch(Exception e){
                 e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jtxtNombreCompletoKeyTyped

