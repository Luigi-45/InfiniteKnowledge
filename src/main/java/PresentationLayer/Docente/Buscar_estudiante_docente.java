    private void jtblMiembroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtblMiembroMouseClicked
        try{
            EstudianteBO estudianteBO = new EstudianteBO();
            Gestionar_calificaciones_docente ventana = new Gestionar_calificaciones_docente();
            ventana.setDni(this.getDni());
            ventana.setNombreCompleto(this.getNombreCompleto());
            ventana.setCorreoElectronico(this.getCorreoElectronico());
            ventana.setEstudiante(estudianteBO.buscar_por_dni((String) this.modelo.getValueAt(this.jtblMiembro.getSelectedRow(), 0)));
            ventana.setVisible(true);
            this.dispose();
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jtblMiembroMouseClicked

